#include "libft.h"

int	intlen(int nbr)
{
	int			i;
	long int	lnbr;

	i = 0;
	if (nbr == 0)
		return (1);
	if (nbr < 0)
	{
		lnbr = nbr;
		lnbr *= -1;
		i++;
	}
	else
		lnbr = nbr;
	while (lnbr > 0)
	{
		lnbr /= 10;
		i++;
	}
	return (i);
}
